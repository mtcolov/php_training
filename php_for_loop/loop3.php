<?php
for($i=1; $i <= 5; $i++) { //отпечатва 5 пъти вложения цикъл
	echo "<p>";
	for($j=1; $j <= $i; $j++) { //отпечатва 5 *
		echo "*";
		if ($j < $i) {
		echo " ";
		}
}
echo "</p>";
}
for($i=5; $i >= 1; $i--) { //отпечатва 5 пъти вложения цикъл
	echo "<p>";
	for($j=1; $j <= $i; $j++) { //отпечатва 5 *
		echo "*";
		if ($j < $i) {
		echo " ";
		}
}
echo "</p>";
}